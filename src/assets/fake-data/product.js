const getProducts = (count, slug) => {
    const temp = products.filter(item => item.categorySlug.includes(slug[0]))
    const max = temp.length - count;
    const min = 0;
    const start = Math.floor(Math.random() * (max - min) + min)
    return temp.slice(start, start + count)
}

const getProductBySlug = (slug) => products.find(e => e.slug === slug)

const getCartItemsDetail = (cartItems) => {
    let res = []
    if (cartItems.length > 0) {
        cartItems.forEach(e => {
            res.push({
                ...e,
                product: getProductBySlug(e.slug)
            })
        })
    }
    return res
}

